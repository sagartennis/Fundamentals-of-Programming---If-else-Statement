public class ifstatement {
    public static void main(String[] args) {
        // Intialize int a and int b to 4 and 5 respectively 
        int a = 5;
        int b = 4;

        if (a > b){
            System.out.println(a + " is greater than " + b );
        } else{
            System.out.println(b + " is greater than " + a );
        }
    }
}
